package com.example.bottomnavigationdemo.Useless;

public class Card {
    private String headPortraitName;
    private String name;
    private String title;
    private String info;
    private String giftPictureName;
    private int index;

    public String getHeadPortraitName() {
        return headPortraitName;
    }

    public void setHeadPortraitName(String headPortraitName) {
        this.headPortraitName = headPortraitName;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getGiftPictureName() {
        return giftPictureName;
    }

    public void setGiftPictureName(String giftPictureName) {
        this.giftPictureName = giftPictureName;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public int getIndex() {
        return index;
    }
}
